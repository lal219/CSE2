public class TextSpinner{
    public static void main(String [] args){
        int constantLoop = 1;
        while (constantLoop == 1){
            System.out.print("/");
            System.out.print("\b");
            System.out.print("-");
            System.out.print("\b");
            System.out.print("\\");
            System.out.print("\b");
            System.out.print("|");
            System.out.print("\b");
        }
    }
}
    